-- Create TEAMMATE_VOICES user and grant permissions
CREATE USER TEAMMATE_VOICES IDENTIFIED BY teammate123;
GRANT CONNECT, RESOURCE, DBA TO TEAMMATE_VOICES;
GRANT UNLIMITED TABLESPACE TO TEAMMATE_VOICES;

-- Create survey tables
CREATE TABLE TEAMMATE_VOICES.SURVEYS (
    survey_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) NOT NULL,
    description VARCHAR2(1000),
    is_published NUMBER(1) DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE TEAMMATE_VOICES.SURVEY_QUESTIONS (
    question_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    survey_id NUMBER NOT NULL,
    question_text VARCHAR2(1000) NOT NULL,
    question_type VARCHAR2(50) NOT NULL,
    is_required NUMBER(1) DEFAULT 0,
    display_order NUMBER DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_question_survey FOREIGN KEY (survey_id) REFERENCES TEAMMATE_VOICES.SURVEYS(survey_id) ON DELETE CASCADE
);

CREATE TABLE TEAMMATE_VOICES.SURVEY_OPTIONS (
    option_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_id NUMBER NOT NULL,
    option_text VARCHAR2(500) NOT NULL,
    display_order NUMBER DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_option_question FOREIGN KEY (question_id) REFERENCES TEAMMATE_VOICES.SURVEY_QUESTIONS(question_id) ON DELETE CASCADE
);

-- Create indexes
CREATE INDEX idx_survey_questions_survey_id ON TEAMMATE_VOICES.SURVEY_QUESTIONS(survey_id);
CREATE INDEX idx_survey_options_question_id ON TEAMMATE_VOICES.SURVEY_OPTIONS(question_id);
